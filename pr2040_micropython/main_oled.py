OLED_ADDR = 0x3C
OLED_WIDTH = 128
OLED_HEIGHT = 64

# ===== 初始化 UART / LED ======================================
uart = UART(UART_ID, baudrate=BAUDRATE, tx=Pin(0), rx=Pin(1))
led  = Pin(LED_PIN, Pin.OUT)
led.off()

# ===== 初始化 I2C OLED ========================================
i2c = I2C(I2C_ID, scl=Pin(5), sda=Pin(4), freq=400000)

# SSD1306 驱动
def oled_init():
    cmds = [
        0xAE,       # display off
        0xD5, 0x80, # clock div
        0xA8, 0x3F, # mux ratio
        0xD3, 0x00, # display offset
        0x40,       # start line
        0x8D, 0x14, # charge pump
        0x20, 0x00, # memory mode: horizontal
        0xA1,       # seg remap
        0xC8,       # com scan direction
        0xDA, 0x12, # com pins
        0x81, 0xCF, # contrast
        0xD9, 0xF1, # pre-charge
        0xDB, 0x40, # vcom detect
        0xA4,       # display all on resume
        0xA6,       # normal display
        0xAF,       # display on
    ]
    for c in cmds:
        i2c.writeto(OLED_ADDR, bytes([0x00, c]))

def oled_data(data):
    """发送像素数据到 OLED"""
    for i in range(0, len(data), 32):
        i2c.writeto(OLED_ADDR, bytes([0x40]) + data[i:i+32])

