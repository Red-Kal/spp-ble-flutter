def char_pixels(ch):
    """返回字符的 6x8 像素数据"""
    idx = ord(ch) - 32
    if idx < 0 or idx >= len(FONT6x8):
        return [0] * 6
    return [FONT6x8[idx * 6 + i] for i in range(6)]

def text_pixels(text, max_w=128):
    """将文本转为按列排列的像素数据，每列 8 像素"""
    cols = []
    for ch in text:
        cols.extend(char_pixels(ch))
        cols.append(0)  # 字间距 1 列
    return cols[:max_w]

# OLED 帧缓冲 (128x64, 按列排列)
oled_frame = bytearray(OLED_WIDTH * 8)  # 128列 * 8页 = 1024字节

def oled_clear():
    for i in range(len(oled_frame)):
        oled_frame[i] = 0

def oled_scroll_up(lines=1):
    """向上滚动 lines 行(每行 8 像素/1 页=1 行文字)"""
    shift = lines * OLED_WIDTH  # 一行 = 128 字节
    for i in range(len(oled_frame) - shift):
        oled_frame[i] = oled_frame[i + shift]
    for i in range(len(oled_frame) - shift, len(oled_frame)):
        oled_frame[i] = 0

def oled_draw_text_at(text, page, col=0):
    """在指定行(page 0-7)绘制文本"""
    pixels = text_pixels(text, OLED_WIDTH - col)
    offset = page * OLED_WIDTH + col
    for i, p in enumerate(pixels):
        if offset + i < len(oled_frame):
            oled_frame[offset + i] = p

def oled_flush():
    """将帧缓冲发送到 OLED"""
    for page in range(8):
        # 设置页地址
        i2c.writeto(OLED_ADDR, bytes([0x00, 0xB0 + page]))
        i2c.writeto(OLED_ADDR, bytes([0x00, 0x00]))  # col low
        i2c.writeto(OLED_ADDR, bytes([0x00, 0x10]))  # col high
        # 发送该页数据
        start = page * OLED_WIDTH
        oled_data(oled_frame[start:start + OLED_WIDTH])

# OLED 文本行缓冲区 (滚动用)
oled_lines = []  # 存储每行文本
MAX_LINES = 8   # OLED 最多显示 8 行 (64/8)

def oled_add_line(text):
    """添加一行文本到 OLED，自动滚动"""
    global oled_lines
    oled_lines.append(text)
    if len(oled_lines) > MAX_LINES:
        oled_lines = oled_lines[-MAX_LINES:]
    # 重绘
    oled_clear()
    for i, line in enumerate(oled_lines):
        oled_draw_text_at(line, i)
    oled_flush()

# ===== 初始化 OLED ============================================
oled_init()
oled_clear()
oled_draw_text_at("BT37 + OLED", 0)
oled_draw_text_at("等待连接...", 2)
oled_flush()

# ===== LED 闪烁 ==============================================
def blink(n):
    if n <= 0: return
    if n > MAX_BLINK: n = MAX_BLINK
    for _ in range(n):
        led.on()
        time.sleep_ms(BLINK_MS)
        led.off()
        time.sleep_ms(BLINK_MS)

blink(3)

# ===== USB 输入 ==============================================
usb_poller = select.poll()
usb_poller.register(sys.stdin, select.POLLIN)

# ===== 数据处理 ==============================================
uart_buf = b""
last_recv = 0

def handle_received(raw):
    if not raw or len(raw) == 0:
        return
    try:
        text = raw.decode("utf-8")
    except:
        text = str(raw)
    n = len(text)
    print("[手机] " + text + "  (" + str(n) + "字符)")
    blink(n)
    # OLED 显示
    oled_add_line("> " + text[:20])
    # 自动回复
    reply = "Pico(" + str(n) + "): " + text + "\r\n"
    uart.write(reply)

oled_add_line("系统就绪")
oled_add_line("等待数据...")

# ===== 主循环 ================================================
while True:
    now = time.ticks_ms()

    # 手机 → BT37 → Pico
    if uart.any():
        chunk = uart.read()
        if chunk:
            uart_buf += chunk
            last_recv = now
            while b"\n" in uart_buf:
                line, uart_buf = uart_buf.split(b"\n", 1)
                line = line.strip()
                if line:
                    handle_received(line)

    # 超时兜底
    if uart_buf and (now - last_recv > 200):
        handle_received(uart_buf.strip())
        uart_buf = b""

    # Thonny → 手机
    if usb_poller.poll(0):
        usb_line = sys.stdin.readline()
        if usb_line:
            usb_line = usb_line.strip()
            if usb_line:
                uart.write(usb_line + "\r\n")
                print("[USB→手机] " + usb_line)
                oled_add_line("<< " + usb_line[:20])

    time.sleep_ms(5)
